<p>Создай админ-панель для управления спортивными лигами, турнирами и командами.</p>

<h3>Сущности и связи:</h3>
<p><strong>Вид спорта</strong></p>
<ul>
    <li>Поля: Название, иконка (опционально).</li>
    <li>Связи: Один вид спорта → много лиг.</li>
</ul>

<p><strong>Лига</strong></p>
<ul>
    <li>Поля: Название, описание, логотип (image), вид спорта (BelongsTo).</li>
    <li>Связи: Лига → много турниров.</li>
</ul>

<p><strong>Турнир</strong></p>
<ul>
    <li>Поля: Название, лига (BelongsTo), город (BelongsTo), даты (start/end), место проведения, этапы (JSON или текстовое поле для описания, например "Групповой этап, Плей-офф").</li>
</ul>

<p><strong>Связи:</strong></p>
<ul>
    <li>Турнир → много этапов (HasMany).</li>
    <li>Турнир → много команд (ManyToMany).</li>
    <li>Турнир → итоги (HasOne для подиума: 1-е, 2-е, 3-е места).</li>
</ul>

<p><strong>Этап</strong></p>
<ul>
    <li>Поля: Название, турнир (BelongsTo), дата начала, дата окончания.</li>
    <li>Связи: Этап → много матчей (HasMany).</li>
</ul>

<p><strong>Игра (Game)</strong></p>
<ul>
    <li>Поля: Этап (BelongsTo), команда_дома (BelongsTo), команда_гостя (BelongsTo), счёт (например "3:2"), дата и время.</li>
</ul>

<p><strong>Команда</strong></p>
<ul>
    <li>Поля: Название, описание, логотип (image), город (BelongsTo), вид спорта (BelongsTo).</li>
    <li>Связи: Команда → много участников (HasMany).</li>
</ul>

<p><strong>Участник</strong></p>
<ul>
    <li>Поля: Имя, фамилия, фото (image), пол (enum: male/female), контакты, команда (BelongsTo).</li>
</ul>

<p><strong>Итоги турнира</strong></p>
<ul>
    <li>Поля: Турнир (BelongsTo), команда, результат в свободной форме, поле для сортировки записей</li>
</ul>

<p><strong>Город</strong></p>
<ul>
    <li>Поля: Название, страна</li>
</ul>
